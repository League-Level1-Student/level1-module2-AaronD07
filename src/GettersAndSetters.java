
public class GettersAndSetters {
	private String meat = "beef";
	private String sauce = "Salsa";

	public String getMeat() {
		return meat;
	}

	public String getSauce() {
		return sauce;
	}

	public void setMeat(String meat) {
		this.meat = meat;
	}

	public void setSauce(String sauce) {
		this.sauce = sauce;
	}

	
}
